package fx2023_3_26;

public class autoConvert {
    public static void main(String[] args) {
        //数据类型转换（自动类型转化
        //byte short int long flout double boolean char
        double d = 100;
        int i = 'a';
        System.out.println(d);
        System.out.println(i);

        int a = 100;
        float f = 10.1f;
        System.out.println(a+f+d);
    }
}
